from math import *

my_num = 5

print(str(my_num) + " is the favourite number")  # converts number to string so it can be concatenated
print(abs(-10.3))  # returns the absolute value
print(pow(3, 2))  # 3^2 = 9
print(max(30, 30.1))  # returns the largest value
print(min(30, 30.1, 28))  # returns the smallest value

print(round(3.3))  # rounds the number to the nearest integer
print(round(3.14159, 2))  # rounds to 2 decimal places

print(floor(3.7))  # rounds down to the nearest integer
print(ceil(3.1))   # rounds up to the nearest integer
print(sqrt(49))    # returns the square root

print(factorial(5))  # returns 5! = 120
print(exp(2))  # e^2 (Euler's number raised to power 2)
print(log(10))  # natural logarithm (base e)
print(log2(32))  # log base 2 → 5
print(log10(1000))  # logarithm base 10

print(sin(0))  # sine of angle in radians
print(cos(0))  # cosine of angle in radians
print(tan(0))  # tangent of angle in radians

print(radians(180))  # converts degrees to radians
print(degrees(pi))  # converts radians to degrees

print(pi)  # constant π
print(round(pi,2))  # constant π
print(e)  # constant e (Euler's number)